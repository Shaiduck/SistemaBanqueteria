    private void dameEstado() throws SQLException{
        ResultSet rs;
        String sql= "Select * from estado";
        PreparedStatement sqls= cn.prepareStatement(sql);
        rs= sqls.executeQuery();
        
        while(rs.next()){
            estado cb= new estado();
            cb.setNombreestado(rs.getString("nombreestado"));
            cmbEstado.addItem(cb);
        }
    }
    
    private void dameTarea() throws SQLException{
        ResultSet rs;
        String sql= "SELECT * FROM tareas WHERE fkidagenda='"+TareasEvento.jTextField1.getText()+"'";
        PreparedStatement sqls=cn.prepareStatement(sql);
        rs=sqls.executeQuery();
        
        while(rs.next()){
            ComboNombre cmb= new ComboNombre();
            cmb.setNombre(rs.getString("nombre_tarea"));
            cmbModif.addItem(cmb);
        }
    }
