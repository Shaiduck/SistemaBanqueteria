    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        try{
            int uno=1;
            int dos=2;
            int tres=3;
            String estado= cmbEstado.getSelectedItem().toString();
            if("Pendiente".equals(estado)){
                txtnum.setText(String.valueOf(uno));
            }else{
                if("En Produccion".equals(estado)){
                    txtnum.setText(String.valueOf(dos));
                }else{
                    if("Realizada".equals(estado)){
                        txtnum.setText(String.valueOf(tres));
                    }
                }
            }
            PreparedStatement pst= cn.prepareStatement("UPDATE Tareas SET estado_tarea='"+txtnum.getText()+
                    "' WHERE nombre_tarea='"+cmbModif.getSelectedItem()+"'" );
            pst.executeUpdate();
            JOptionPane.showOptionDialog(this, "Actualizado correctamente", "Confirmacion", JOptionPane.INFORMATION_MESSAGE, JOptionPane.INFORMATION_MESSAGE, null, new Object[]{" OK "},"OK");
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

